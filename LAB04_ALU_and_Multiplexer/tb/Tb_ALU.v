`timescale 1ns / 1ps
module tb_ALU();
    reg [31:0] A;
    reg [31:0] B;
    reg M;
    reg S1;
    reg S0;
    wire [31:0] result;
    wire Overflow;
    ALU dut (
        .A(A),
        .B(B),
        .M(M),
        .S1(S1),
        .S0(S0),
        .result(result),
        .Overflow(Overflow)
    );

    initial begin
        $display("Time | M S1 S0 | A | B | Result | Overflow");
        $monitor("%4t | %b  %b  %b | %b | %b | %b |    %b", 
                 $time, M, S1, S0, A, B, result, Overflow);
        A = 32'b0000_0000_0000_0000_0000_0000_0000_1010;
        B = 32'b0000_0000_0000_0000_0000_0000_0000_0011;
        M = 0; S1 = 0; S0 = 0; 
        #10; {M, S1, S0} = 3'b000; 
        #10; {M, S1, S0} = 3'b001; 
        #10; {M, S1, S0} = 3'b010; 
        #10; {M, S1, S0} = 3'b011; 
        #10; {M, S1, S0} = 3'b100; 
        #10; {M, S1, S0} = 3'b111;
        #10; 
        A = 32'b0000_0000_0000_0000_0000_0000_0110_0100; 
        B = 32'b0000_0000_0000_0000_0000_0000_0011_0010; 
        {M, S1, S0} = 3'b101; 
        #10; 
        A = 32'b0111_1111_1111_1111_1111_1111_1111_1111; 
        B = 32'b0000_0000_0000_0000_0000_0000_0000_0001; 
        {M, S1, S0} = 3'b101;
        #10; 
        A = 32'b0000_0000_0000_0000_0000_0000_0110_0100; 
        B = 32'b0000_0000_0000_0000_0000_0000_0011_0010; 
        {M, S1, S0} = 3'b110;
        #10; 
        A = 32'b1000_0000_0000_0000_0000_0000_0000_0000; 
        B = 32'b0000_0000_0000_0000_0000_0000_0000_0001; 
        {M, S1, S0} = 3'b110;
        #20;
        $stop;
    end
endmodule